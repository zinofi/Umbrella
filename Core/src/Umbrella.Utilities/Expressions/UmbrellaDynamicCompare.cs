namespace Umbrella.Utilities.Expressions;

/// <summary>
/// Define a comparison method.
/// </summary>
public enum UmbrellaDynamicCompare
{
	/// <summary>
	/// An equal comparison.
	/// </summary>
	Equal = 0,

	/// <summary>
	/// A not equal comparison.
	/// </summary>
	NotEqual = 1,

	/// <summary>
	/// A greater than comparison.
	/// </summary>
	GreaterThan = 2,

	/// <summary>
	/// A greater than or equal comparison.
	/// </summary>
	GreaterThanOrEqual = 3,

	/// <summary>
	/// A less than comparison.
	/// </summary>
	LessThan = 4,

	/// <summary>
	/// A less than or equal comparison.
	/// </summary>
	LessThanOrEqual = 5
}